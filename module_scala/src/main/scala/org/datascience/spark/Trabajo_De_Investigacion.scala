package org.datascience.spark

object Trabajo_De_Investigacion {

  def main(args: Array[String]): Unit = {

    // Este método se utiliza para devolver el código hash de una string.
    // Scala program of hashCode() method
    // Using hashCode() methods
    val result = "Ayushi".hashCode()
    println("Result : " + result)

    //Este método se utiliza para la concatenación de las dos strings. Unió dos strings juntas y formó una sola string
    // Concatenate two strings
    // Using concat() methods
    val result1 = "Geeks".concat("forGeeks")
    println("Result : " + result1)


    // Este metodo sirve para reemplazar dos caracteres
    val result3 = "potdotnothotokayslot".replaceFirst(".ot", "**")
    println("Result : " + result3)

  }



}
